#include <unistd.h>

void	ft_putchar(char n)
{
	write(1, &n, 1);
}

void	ft_putnbr(int nb)
{
	if(nb == -2147483647)
	{
		write(1, "-2147483647", 10);
	}
	if(nb > 0)
	{
		ft_putchar((nb / 10) + '0');
		ft_putchar((nb % 10) + '0');
	}
}

int	main(void)
{
	ft_putnbr(321);
}
